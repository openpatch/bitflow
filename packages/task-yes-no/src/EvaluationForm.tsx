import { Box, Checkbox, HookFormController } from "@openpatch/patches";
import { useTranslations } from "@vocab/react";
import translations from "./locales.vocab";
import { TaskBit } from "./types";

export const EvaluationForm: TaskBit["EvaluationForm"] = ({ name }) => {
  const { t } = useTranslations(translations);

  return (
    <HookFormController
      name={`${name}.evaluation.yes`}
      defaultValue={false}
      render={({ value, onChange, onBlur }) => (
        <Box mt="standard">
          <Checkbox checked={value} onChange={onChange} onBlur={onBlur}>
            {t("yes")}
          </Checkbox>
        </Box>
      )}
    />
  );
};
